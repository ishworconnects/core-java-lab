public class MyMathWork {
    public static void main(String[] args) {
        int num1=10;
        int num2=50;

        System.out.println("Max num: " + Math.max(num1,num2));
        System.out.println("Min num: " + Math.min(num1,num2));
        System.out.println("Round Value of 11.6 :" + Math.round(11.5));

        System.out.println("Floor Value: " +Math.floor(10.8));
        System.out.println("Ceiling Value: " +Math.ceil(10.8));
        System.out.println("Square Root Value: " +Math.sqrt(25));
        System.out.println("Cube :" + Math.pow(5,3));


    }
}
