public class OperatorWork {
    public static void main(String[] args) {
        int num = 100;
        num++; //it gives addition
        //also we can write this code as
        // num = num + 1
        num--;//it gives subtraction

        //result will be same becuase first it add and then subtract
        System.out.println(num);
        System.out.println(num == 100);
        System.out.println(num != 100);
        System.out.println(num > 100);
    }

}
